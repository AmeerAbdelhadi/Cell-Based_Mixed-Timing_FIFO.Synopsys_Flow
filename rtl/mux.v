module mux
  #(  parameter DW = 32  )  // data width
   (  input           sel,  // selector
      input  [DW-1:0] in0,  // input 0
      input  [DW-1:0] in1,  // input 1
      output [DW-1:0] out); // output

  assign out = sel ? in1 : in0;

endmodule
